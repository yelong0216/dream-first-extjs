package dream.first.extjs.platform.icon.code;

/**
 * 图标代码管理器异常
 * 
 * @since 2.0
 */
public class IconCodeManageException extends RuntimeException {

	private static final long serialVersionUID = 3790761760569453568L;

	public IconCodeManageException() {
		super();
	}

	public IconCodeManageException(String message, Throwable cause) {
		super(message, cause);
	}

	public IconCodeManageException(String message) {
		super(message);
	}

	public IconCodeManageException(Throwable cause) {
		super(cause);
	}

}
